var pangkatAngka= function(base,exponent){
  return Math.pow(base,exponent);
}
console.log(pangkatAngka(2,3));

var akarKuadrat = function(base){
  return Math.pow(base,2);
}
console.log(akarKuadrat(2));

var hipotenusa = function(A,B){
  csquare = A*A+B*B;
  return Math.pow(csquare,0.5);
}
console.log(hipotenusa(3,4));

var luasLingkaran = function(r){
  return 22/7*r*r;
}
console.log(luasLingkaran(7));